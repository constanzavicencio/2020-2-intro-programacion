# buscamos una funci√≥n que, a partir de una lista de productos, calcule el costo total de la compra
# esto es, la sumatoria de producto por cantidad en la lista "carrito"
# por ejemplo, lista(producto("chocolate",1200,3), lista(producto("destacadores", 750, 6),lista(producto("bidon de agua",3000,1), None)))
# deberia retornar 11100
def totalCarrito(unaLista):
  if vacia(unaLista):
    return 0
  else:
    producto=cabeza(unaLista)
    return producto.precio*producto.cantidad + totalCarrito(cola(unaLista))

